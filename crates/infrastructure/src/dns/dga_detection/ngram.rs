/// Computes bigram deviation score for an SLD.
///
/// Returns 0.0 (English-like) to 1.0 (maximally random/DGA-like).
/// Non-alpha characters (digits, hyphens) reset the bigram pair.
pub fn bigram_deviation_score(sld: &str) -> f32 {
    let bytes = sld.as_bytes();
    if bytes.len() < 2 {
        return 0.0;
    }

    let mut total_log_prob: f32 = 0.0;
    let mut pair_count: u32 = 0;
    let mut prev: Option<u8> = None;

    for &b in bytes {
        let lower = b.to_ascii_lowercase();
        if lower.is_ascii_lowercase() {
            if let Some(p) = prev {
                let i = (p - b'a') as usize;
                let j = (lower - b'a') as usize;
                total_log_prob += ENGLISH_BIGRAM_LOG_PROB[i][j];
                pair_count += 1;
            }
            prev = Some(lower);
        } else {
            prev = None; // reset pair on non-alpha
        }
    }

    if pair_count == 0 {
        return 0.5; // no pairs — ambiguous
    }

    let avg_log_prob = total_log_prob / pair_count as f32;

    // Normalize: English baseline ~-3.2, random baseline ~-4.8
    // Score: 0.0 = English-like, 1.0 = maximally random
    const ENGLISH_BASELINE: f32 = -3.2;
    const RANDOM_BASELINE: f32 = -4.8;

    let score = (avg_log_prob - ENGLISH_BASELINE) / (RANDOM_BASELINE - ENGLISH_BASELINE);
    score.clamp(0.0, 1.0)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn english_words_score_low() {
        let score = bigram_deviation_score("google");
        assert!(score < 0.4, "google scored {score}, expected < 0.4");

        let score = bigram_deviation_score("facebook");
        assert!(score < 0.4, "facebook scored {score}, expected < 0.4");

        let score = bigram_deviation_score("amazon");
        assert!(score < 0.4, "amazon scored {score}, expected < 0.4");
    }

    #[test]
    fn random_strings_score_high() {
        let score = bigram_deviation_score("xjkfqazh");
        assert!(score > 0.5, "xjkfqazh scored {score}, expected > 0.5");

        let score = bigram_deviation_score("qwrtplkm");
        assert!(score > 0.5, "qwrtplkm scored {score}, expected > 0.5");
    }

    #[test]
    fn short_sld_returns_zero() {
        assert!((bigram_deviation_score("a") - 0.0).abs() < f32::EPSILON);
    }

    #[test]
    fn digits_reset_pairs() {
        // "ab3cd" → pairs: (a,b), (c,d) — the '3' resets
        let score = bigram_deviation_score("ab3cd");
        assert!((0.0..=1.0).contains(&score));
    }
}
